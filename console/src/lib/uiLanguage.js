export const UI_LANGUAGE_STORAGE_KEY = 'gdpro_ui_language';

export const UI_LANGUAGES = [
  { id: 'zh', label: '中文', shortLabel: '中', locale: 'zh-CN' },
  { id: 'en', label: 'English', shortLabel: 'EN', locale: 'en' },
];

export function normalizeUiLanguage(value) {
  return value === 'en' ? 'en' : 'zh';
}

export function loadUiLanguage(fallback = 'zh') {
  if (typeof window === 'undefined') return normalizeUiLanguage(fallback);
  try {
    return normalizeUiLanguage(window.localStorage.getItem(UI_LANGUAGE_STORAGE_KEY) || fallback);
  } catch {
    return normalizeUiLanguage(fallback);
  }
}

export function saveUiLanguage(value) {
  const language = normalizeUiLanguage(value);
  if (typeof window !== 'undefined') {
    try {
      window.localStorage.setItem(UI_LANGUAGE_STORAGE_KEY, language);
    } catch {
      // Local storage can be unavailable in restricted embeds.
    }
  }
  return language;
}

export function otherUiLanguage(value) {
  return normalizeUiLanguage(value) === 'en' ? 'zh' : 'en';
}

export function uiText(group, language) {
  const copy = UI_COPY[group] || {};
  return copy[normalizeUiLanguage(language)] || copy.zh || copy.en || {};
}

